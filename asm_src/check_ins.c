#include "asm.h"

int		check_opcode(t_instru *ins)
{
	int		i;

	i = 0;
	while (i < 16)
	{
		if (ft_strcmp(g_op_tab[i].name, ins->opcode) == 0)
			return (1);
		i++;
	}
	return (0);
}
