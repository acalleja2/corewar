#include "asm.h"

void	add_n(char *buf, char **nm, char **str)
{
	char	*tmp;

	tmp = *nm;
	buf[0] = '\n';
	*nm = ft_strjoin(*nm, buf);
	free(tmp);
	free(*str);
}
