#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	if (!s1 && !s2)
		return (TRUE);
	if (!s1 || !s2)
		return (FALSE);
	while (*s1 == *s2 && *s1 && *s2)
	{
		s1 += 1;
		s2 += 1;
	}
	return (*s1 == *s2);
}
