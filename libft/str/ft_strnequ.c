#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if ((!s1 && !s2) || !n)
		return (TRUE);
	if (!s1 || !s2)
		return (FALSE);
	while (*s1 == *s2 && *s1 && *s2 && n - 1 > 0)
	{
		s1++;
		s2++;
		n--;
	}
	return (*s1 == *s2);
}
