#include "libft.h"

/*
** Les caracteres a sauter pour le atoi()
*/

int		ft_is_toskip(char c)
{
	return (c == ' ' || c == '\t' || c == '\n' || c == '\f' || c == '\r' ||
			c == '\v');
}
